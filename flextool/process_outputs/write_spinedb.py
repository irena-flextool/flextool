def _serialize_total_series(series, spec):
    """Serialize ``costs_discounted_p_`` (a per-category Series, no period
    axis) into one ``Map(category -> value)`` for entity ``("model",)``."""
    if isinstance(series, pd.DataFrame):
        # to_frame() of a Series yields a 1-column frame; squeeze it back.
        if series.shape[1] == 1:
            series = series.iloc[:, 0]
        else:
            return []
    keys, vals = [], []
    for cat, val in series.items():
        if _is_empty(val):
            continue
        keys.append(_cat_key_str(cat))
        vals.append(float(val))
    if not keys:
        return []
    return [(spec["ec"], spec["fixed_entity"], Map(keys, vals))]


def _serialize_entity_index(df, spec, s, solve_name):
    """Serialize an entity-in-index frame (capacity / indicator): index is
    ``(entity[, period[, time]])`` and columns are categories.

    Emits ``(ec, (entity,), Map(category -> [solve ->] period [-> time]))``
    per entity, dropping all-empty cells / categories."""
    ec = spec["ec"]
    has_time = spec["has_time"]
    entity_level = spec["entity_index"]
    if entity_level not in (df.index.names or []):
        logger.debug("spinedb: entity level '%s' not in index of '%s' — skipped",
                     entity_level, spec["param"])
        return []

    result: list[tuple] = []
    for entity, sub in df.groupby(level=entity_level, sort=False):
        sub = sub.droplevel(entity_level)
        sub = _ensure_solve_level(sub, s, has_time)
        cat_keys: list[str] = []
        cat_vals: list = []
        for col in sub.columns:
            value = _series_to_map(sub[col], has_time, None,
                                   collapse_solve=False, solve_name=solve_name)
            if value is None:
                continue
            cat_keys.append(_cat_key_str(col))
            cat_vals.append(value)
        if not cat_keys:
            continue
        result.append((ec, (entity,), Map(cat_keys, cat_vals)))
    return result


def _discount_factor_map(par, attr, s, solve_name):
    """Build a ``Map(solve -> period -> value)`` from a per-period Series on
    ``par`` (the inflation factors).  Returns None when unavailable/empty."""
    series = getattr(par, attr, None)
    if series is None:
        return None
    if isinstance(series, pd.DataFrame):
        if series.shape[1] != 1:
            return None
        series = series.iloc[:, 0]
    if not isinstance(series, pd.Series) or series.empty:
        return None
    # Period -> solve reconstruction, then nest solve -> period -> value.
    period_to_solve = None
    sp = getattr(s, "solve_period", None)
    if sp is not None:
        unique_sp = sp[~sp.droplevel("solve").duplicated(keep="last")]
        period_to_solve = dict(zip(unique_sp.get_level_values("period"),
                                   unique_sp.get_level_values("solve")))
    by_solve: dict[str, list] = {}
    order: list[str] = []
    for period, val in series.items():
        if _is_empty(val):
            continue
        solve = (period_to_solve.get(period) if period_to_solve else None) \
            or solve_name
        solve = str(solve)
        if solve not in by_solve:
            by_solve[solve] = ([], [])
            order.append(solve)
        by_solve[solve][0].append(str(period))
        by_solve[solve][1].append(float(val))
    if not order:
        return None
    solve_keys, solve_vals = [], []
    for solve in order:
        pk, pv = by_solve[solve]
        solve_keys.append(solve)
        solve_vals.append(Map(pk, pv))
    return Map(solve_keys, solve_vals)


