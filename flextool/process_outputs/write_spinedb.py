def derive_alternative_name(scenario_name: str, solve_name: str) -> str:
    """Return the Spine alternative name for a run.

    v1: deterministically returns ``scenario_name`` (no timestamp / random,
    honoring cross-process determinism).  Multiple solves coexist inside the
    ``solve`` Map axis of each value; multiple runs coexist as distinct
    alternatives.  A future ``--results-alternative`` override hooks here.
    """
    return scenario_name


def ensure_results_db(db_url: str) -> None:
    """Create the results DB from the schema JSON if it is absent; no-op if
    it already exists.

    The schema JSON is fed straight to ``import_data`` (its top-level keys
    ``entity_classes`` / ``parameter_definitions`` / ``alternatives`` are
    valid ``import_data`` kwargs).  ``import_data`` of classes/params is
    idempotent, so re-running against an existing DB would be harmless, but
    we short-circuit on an existing sqlite file for speed.

    Concurrency contract (the Toolbox re-create context launches parallel
    scenario iterations into ONE shared ``results.sqlite``):

    * **Creation serializes via a sidecar lock.**  For ``sqlite:///`` targets
      the create-if-absent is guarded by an exclusive sidecar lock on
      ``<db>.lock`` (``fcntl.flock`` on POSIX, ``msvcrt.locking`` on
      Windows), so exactly ONE process
      ever builds the schema.  Inside the lock we re-check for the target and,
      if another process already created it, discard our temp build and NEVER
      ``os.replace`` over it — so a late rename can never clobber a DB another
      process has already begun importing into.  The schema is built into a
      unique temp file in the target directory and ``os.replace``-d into place
      (atomic on the same filesystem) only when the target is still absent.
    * **Subsequent per-process IMPORTs append and serialize via the sqlite
      busy-timeout.**  Each scenario writes its own alternative through
      ``write_spinedb`` -> ``import_data``; concurrent appends to the shared
      sqlite file are serialized by spinedb_api's connection busy-timeout —
      ``DatabaseMapping.create_engine`` passes ``connect_args={"timeout":
      sqlite_timeout}`` (default 1800 s), the pysqlite busy-timeout, so a
      writer that finds the DB locked WAITS and retries rather than failing
      with ``SQLITE_BUSY``.  We therefore do NOT hold the creation lock across
      the import — appends are independent writers and only the cold-start
      create must be single-writer.

    Non-sqlite URLs (e.g. a shared server) fall back to the direct in-place
    create (no atomic rename / file lock possible across a network DSN).
    """
    if db_url.startswith("sqlite:///"):
        path = db_url[len("sqlite:///"):]
        # Fast path: a present DB is success without taking the lock.
        if os.path.exists(path):
            return
        _ensure_results_db_atomic(db_url, path)
        return
    # Non-sqlite target: cannot atomically rename; create in place.
    _import_schema(db_url)


def _lock_exclusive(lock_fh) -> None:
    """Acquire a blocking exclusive lock on an open file handle.

    Portable across POSIX (``fcntl.flock``) and Windows (``msvcrt.locking``);
    ``fcntl`` does not exist on Windows, so dispatch on what imports."""
    try:
        import fcntl
    except ImportError:
        import msvcrt
        import time

        # ``msvcrt.locking`` locks ``nbytes`` from the current file position;
        # lock a single byte at offset 0.  Use the non-blocking variant in a
        # retry loop so we block indefinitely rather than giving up after the
        # ~10s LK_LOCK ceiling.
        lock_fh.seek(0)
        while True:
            try:
                msvcrt.locking(lock_fh.fileno(), msvcrt.LK_NBLCK, 1)
                return
            except OSError:
                time.sleep(0.05)
    else:
        fcntl.flock(lock_fh.fileno(), fcntl.LOCK_EX)


def _unlock(lock_fh) -> None:
    """Release a lock taken by :func:`_lock_exclusive` (portable)."""
    try:
        import fcntl
    except ImportError:
        import msvcrt

        lock_fh.seek(0)
        try:
            msvcrt.locking(lock_fh.fileno(), msvcrt.LK_UNLCK, 1)
        except OSError:
            pass
    else:
        fcntl.flock(lock_fh.fileno(), fcntl.LOCK_UN)


def _replace_with_retry(src: str, dst: str, *, attempts: int = 20,
                        delay: float = 0.05) -> None:
    """``os.replace(src, dst)`` retrying transient Windows sharing errors.

    On Windows a file that was just closed can stay briefly locked, so the
    rename raises ``PermissionError`` (WinError 32).  POSIX never hits this,
    so the first attempt always succeeds there."""
    import time

    for i in range(attempts):
        try:
            os.replace(src, dst)
            return
        except PermissionError:
            if i == attempts - 1:
                raise
            time.sleep(delay)


def _ensure_results_db_atomic(db_url: str, path: str) -> None:
    """Create the schema DB at ``path`` if absent, safe under concurrent
    cold-start callers writing the SAME shared results DB.

    The create-if-absent is serialized by an exclusive lock on a sidecar
    ``<db>.lock`` file (``fcntl.flock`` on POSIX, ``msvcrt.locking`` on
    Windows): only the winner of the lock builds the schema.  Both the cheap
    pre-check (in :func:`ensure_results_db`) and the re-check UNDER the lock
    guard against ``os.replace`` clobbering a target another process has
    already created (and may already be importing into) — the rename happens
    only while the target is still absent."""
    import uuid

    target_dir = os.path.dirname(path) or "."
    os.makedirs(target_dir, exist_ok=True)

    lock_path = path + ".lock"
    # The lock file persists (it is the rendez-vous point); only the lock is
    # released.  Opened in append mode so concurrent opens never truncate it.
    with open(lock_path, "a") as lock_fh:
        _lock_exclusive(lock_fh)
        try:
            # Re-check UNDER the lock: another process may have created the
            # target between our pre-check and acquiring the lock.  If so we
            # must NOT build/rename — clobbering it could corrupt an import
            # already in progress in that process.
            if os.path.exists(path):
                return
            tmp_path = os.path.join(
                target_dir, f".results-{uuid.uuid4().hex}.sqlite.tmp")
            tmp_url = "sqlite:///" + tmp_path
            try:
                _import_schema(tmp_url)
                # Still absent (we hold the lock, so it cannot appear now) —
                # commit the build atomically.  Retry the rename briefly: on
                # Windows a just-closed SQLite handle can linger a moment and
                # ``os.replace`` raises ``PermissionError`` (WinError 32) until
                # it is fully released.
                _replace_with_retry(tmp_path, path)
            finally:
                # Discard the temp on any error mid-build.
                if os.path.exists(tmp_path):
                    try:
                        os.remove(tmp_path)
                    except OSError:
                        pass
        finally:
            _unlock(lock_fh)


# ---------------------------------------------------------------------------
# Parameter-definition descriptions: prose from the schema, UNITS from code.
#
# The checked-in results schema carries rich, hand-written prose (per-category
# breakdowns).  We keep that prose but make the **unit** authoritative from the
# single source (``_output_meta``): at DB-create, for every param that maps to
# a known transform, we replace the description's leading ``[unit]`` token with
# the derived one and prepend it where missing.  Empty descriptions are filled
# wholesale.  Params with no mapped transform (e.g. ``cf``) keep their
# hand-written description untouched.  This makes unit drift structurally
# impossible — the units are GENERATED into the schema, never compared against
# it — so the old CI drift ratchet is retired.
# ---------------------------------------------------------------------------

# Leading unit token: ``[MW]``, ``[per unit]``, or the cost tables' ``M[CUR]``.
_LEADING_UNIT_RE = re.compile(r"^\s*(?:M\s*\[[^\]]*\]|\[[^\]]*\])\s*")


def _derived_param_meta() -> tuple[dict[tuple[str, str], tuple[str, str]],
                                   dict[str, tuple[str, str]]]:
    """Build derived ``(unit, short_text)`` keyed by ``(ec, param)`` and an
    unambiguous-by-param fallback.

    The ``(ec, param)`` map drives the exact class; the param-only fallback
    covers classes absent from :data:`SPINEDB_OUTPUT_SPEC` that share a param
    name with a documented one (e.g. the ``connection__reserve__*`` params),
    but only when that param has a single distinct unit across all classes.
    """
    by_ecp: dict[tuple[str, str], tuple[str, str]] = {}
    units_by_param: dict[str, set[str]] = {}
    text_by_param: dict[str, tuple[str, str]] = {}
    for table_name, spec in SPINEDB_OUTPUT_SPEC.items():
        param = spec.get("param")
        summary = result_key_summary(table_name)
        if not param or summary is None:
            continue
        unit, _sem, tooltip = summary
        if unit == "ratio":          # result_key_summary's unitless sentinel
            unit = ""                # → renders as "[per unit]" via _unit_tag
        by_ecp.setdefault((spec.get("ec"), param), (unit, tooltip))
        units_by_param.setdefault(param, set()).add(unit)
        text_by_param.setdefault(param, (unit, tooltip))
    # Params sourced from ``par`` (not a results table): the two discount
    # factors are dimensionless (FACTOR).
    for p in ("investments discount factor", "operations discount factor"):
        units_by_param.setdefault(p, {FACTOR.unit})
        text_by_param.setdefault(p, (FACTOR.unit, FACTOR.tooltip))
    unambiguous = {p: text_by_param[p]
                   for p, us in units_by_param.items() if len(us) == 1}
    return by_ecp, unambiguous


def _unit_tag(unit: str) -> str:
    """The bracketed unit token; unitless quantities render as ``[per unit]``."""
    return f"[{unit}]" if unit else "[per unit]"


def derived_param_unit_map() -> dict[tuple[str, str], str]:
    """``{(entity_class, param): derived_unit}`` for params with a known
    transform — the authoritative units merged into the schema."""
    by_ecp, _unambiguous = _derived_param_meta()
    return {key: unit for key, (unit, _text) in by_ecp.items()}


def _apply_derived_descriptions(schema: dict) -> None:
    """Merge derived units into ``parameter_definition`` descriptions.

    Mutates ``schema['parameter_definitions']`` in place: an entry is
    ``[entity_class, param, default, value_list, description, type]``.  For
    every param with a derived unit, the leading unit token is replaced with
    the derived one (prose preserved); empty descriptions are filled from the
    derived short text.  Params without a derived unit are left untouched."""
    by_ecp, unambiguous = _derived_param_meta()
    for entry in schema.get("parameter_definitions", []):
        ec, param, existing = entry[0], entry[1], entry[4]
        derived = by_ecp.get((ec, param)) or unambiguous.get(param)
        if not derived:
            continue
        unit, short_text = derived
        prose = _LEADING_UNIT_RE.sub("", existing) if existing else short_text
        entry[4] = f"{_unit_tag(unit)} {prose}".strip()


def _import_schema(db_url: str) -> None:
    """Load the results schema JSON into the DB at ``db_url`` (created if
    needed).

    Uses an explicit ``db.close()`` rather than the ``with DatabaseMapping``
    context manager: ``__exit__`` only closes the *session*, leaving the
    engine's connection pool holding the SQLite file open.  ``close()``
    disposes the engine, releasing the handle.  This matters on Windows,
    which refuses to ``os.replace`` a file with an open handle (POSIX has no
    such restriction)."""
    with open(_SCHEMA_PATH, encoding="utf-8") as fh:
        schema = json.load(fh)
    _apply_derived_descriptions(schema)
    db = DatabaseMapping(db_url, create=True)
    try:
        count, errors = import_data(db, **schema)
        if errors:
            raise RuntimeError(
                f"Failed to initialize results schema: {len(errors)} errors: "
                f"{errors[:5]}"
            )
        db.commit_session("Initialized results schema")
        logger.debug("Initialized results DB schema (%d items)", count)
    finally:
        db.close()


def write_spinedb(results, s, db_url, scenario_name, solve_name,
                  *, purge_alternative=True, par=None) -> int:
    """Write the processed ``results`` into the SpineDB at ``db_url``.

    Parameters
    ----------
    results : dict[str, pandas.DataFrame]
        Processed results keyed by RAW engine table name.
    s : object
        The engine ``s`` (sets) namespace.  Used for solve reconstruction
        (``solve_period_time`` / ``solve_period``), process partitioning
        (``process_unit`` / ``process_connection``) and connection topology
        (``process_source_sink``).
    db_url : str
        Target SpineDB URL (e.g. ``sqlite:///.../results.sqlite``).
    scenario_name, solve_name : str
        Run identifiers.  ``scenario_name`` becomes the alternative name;
        ``solve_name`` is used as the ``solve`` Map key for frames whose
        solve axis cannot be reconstructed per-row.
    purge_alternative : bool, keyword-only, default True
        Delete existing ``parameter_value`` items in the target alternative
        before writing (so a re-run replaces rather than duplicates).
    par : object, keyword-only, default None
        The engine ``par`` (parameters) namespace.  Required only for the
        two discount-factor params (#33/#34); when None those are skipped.

    Returns
    -------
    int
        The ``import_data`` count for the parameter-value write.
    """
    ensure_results_db(db_url)
    alternative_name = derive_alternative_name(scenario_name, solve_name)

    if purge_alternative:
        _purge_alternative(db_url, alternative_name)

    entities, parameter_values, entity_alternatives = collect_spinedb_payload(
        results, s, alternative_name, solve_name, par=par,
    )

    with DatabaseMapping(db_url) as db:
        count, errors = import_data(
            db,
            alternatives=[(alternative_name, f"FlexTool results: {alternative_name}")],
            entities=entities,
            parameter_values=parameter_values,
            entity_alternatives=entity_alternatives,
        )
        if errors:
            raise RuntimeError(
                f"Failed to write results to {db_url}: {len(errors)} errors: "
                f"{errors[:5]}"
            )
        db.commit_session(f"FlexTool results: {alternative_name}")

    logger.debug(
        "Wrote %d items to SpineDB alternative '%s' (%d entities, %d values)",
        count, alternative_name, len(entities), len(parameter_values),
    )
    return count


# ---------------------------------------------------------------------------
# Payload collection
# ---------------------------------------------------------------------------

def collect_spinedb_payload(results, s, alternative_name, solve_name,
                            *, par=None):
    """Build the ``(entities, parameter_values, entity_alternatives)`` lists
    for one run by iterating :data:`SPINEDB_OUTPUT_SPEC`.

    Member element entities are collected explicitly (``import_data`` does
    not auto-create them) and deduplicated.  Whitelist keys absent from
    ``results`` (or empty / all-NaN) are skipped with a debug log.
    """
    entity_set: set[tuple[str, tuple]] = set()
    parameter_values: list[tuple] = []

    member_classes = _member_class_map(s)

    for table_name, spec in SPINEDB_OUTPUT_SPEC.items():
        if table_name not in results:
            logger.debug("spinedb: table '%s' absent from results — skipped",
                         table_name)
            continue
        df = results[table_name]
        if df is None:
            continue
        if isinstance(df, pd.Series):
            df = df.to_frame()
        if isinstance(df, pd.DataFrame) and df.empty:
            logger.debug("spinedb: table '%s' empty — skipped", table_name)
            continue
        try:
            pvs = df_to_spine_maps(df, spec, s, solve_name, par=par,
                                   alternative_name=alternative_name)
        except Exception:
            logger.exception(
                "spinedb: failed to serialize table '%s' — skipped",
                table_name)
            continue
        for ec, byname, value in pvs:
            parameter_values.append(
                (ec, byname, spec["param"], value, alternative_name))
            entity_set.add((ec, byname))
            # explicitly emit member element entities
            for member in _member_entities(ec, byname, member_classes):
                entity_set.add(member)

    # Discount factors (#33 / #34) derive from par, not from results.
    if par is not None:
        for fam_attr, param_name in (
            ("inflation_factor_investment_yearly", "investments discount factor"),
            ("inflation_factor_operations_yearly", "operations discount factor"),
        ):
            value = _discount_factor_map(par, fam_attr, s, solve_name)
            if value is not None:
                parameter_values.append(
                    ("model", ("model",), param_name, value, alternative_name))
                entity_set.add(("model", ("model",)))
    else:
        logger.debug("spinedb: par is None — discount-factor params skipped")

    entities = sorted(entity_set, key=lambda x: (x[0], x[1]))
    entity_alternatives = [
        (ec, byname, alternative_name, True) for ec, byname in entities
    ]
    return entities, parameter_values, entity_alternatives


def _member_class_map(s):
    """Map each relationship entity class to the element class of each of
    its dimensions, for explicit member-entity emission."""
    return {
        "unit__node": ("unit", "node"),
        "connection__node__node": ("connection", "node", "node"),
        "group__reserve__upDown": ("reserve", "upDown", "group"),
        "unit__reserve__upDown__node": ("unit", "reserve", "upDown", "node"),
        "connection__reserve__upDown__node":
            ("connection", "reserve", "upDown", "node"),
    }


def _member_entities(ec, byname, member_classes):
    """Yield ``(element_class, (element,))`` for every member of a
    relationship byname.  0-dim classes yield nothing extra (the entity
    itself is already in the set)."""
    dims = member_classes.get(ec)
    if dims is None:
        return
    for elem_class, elem in zip(dims, byname):
        yield (elem_class, (elem,))


# ---------------------------------------------------------------------------
# DataFrame -> Spine Map serialization
# ---------------------------------------------------------------------------

def df_to_spine_maps(df, spec, s, solve_name, *, par=None, alternative_name=None):
    """Serialize one engine DataFrame into a list of
    ``(entity_class, byname, value)`` triples per :data:`SPINEDB_OUTPUT_SPEC`.

    The value is a (possibly nested) :class:`spinedb_api.parameter_value.Map`
    with nesting ``[category ->] [solve ->] period [-> time] -> value`` and
    any category dimension as the OUTERMOST key.
    """
    ec = spec["ec"]
    has_time = spec["has_time"]

    # Special period-less aggregated frames.
    if spec.get("series_category"):
        return _serialize_total_series(df, spec)

    # Entity-in-index frames (capacity, indicator): the entity is an index
    # level, columns are categories.
    if spec.get("entity_index"):
        return _serialize_entity_index(df, spec, s, solve_name)

    df = _ensure_solve_level(df, s, has_time)

    # Reduce the column MultiIndex / Index to a uniform tuple form and
    # split into (entity-part, category-part).  Each yield is
    # ``(byname, cat_key, series, row_ec)`` where ``row_ec`` overrides the
    # spec entity class for that row (reserve-split routing) or is None.
    rows = _iter_columns(df, spec, s)

    out: list[tuple] = []
    for byname, cat_key, series, row_ec in rows:
        if byname is None:
            continue
        value = _series_to_map(series, has_time, cat_key,
                               collapse_solve=spec.get("collapse_solve", False),
                               solve_name=solve_name)
        if value is None:
            continue
        out.append((row_ec or ec, byname, cat_key, value))

    return _assemble_entity_values(out, spec)


def _assemble_entity_values(rows, spec):
    """Combine per-(entity, category) Maps into one value per entity.

    When ``category == "header"`` the category dimension is the outermost
    Map key; otherwise each entity has a single value column.
    """
    use_category = spec.get("category") == "header"
    by_entity: dict[tuple, list[tuple]] = {}
    order: list[tuple] = []
    for row_ec, byname, cat_key, value in rows:
        key = (row_ec, byname)
        if key not in by_entity:
            by_entity[key] = []
            order.append(key)
        by_entity[key].append((cat_key, value))

    result: list[tuple] = []
    for key in order:
        ec, byname = key
        items = by_entity[key]
        if use_category:
            keys, vals = [], []
            for cat_key, value in items:
                if value is None:
                    continue
                keys.append(_cat_key_str(cat_key))
                vals.append(value)
            if not keys:
                continue
            result.append((ec, byname, Map(keys, vals)))
        else:
            # No category nesting: a single value per entity.  If multiple
            # source columns mapped to the same entity (e.g. output + input
            # node frames merged), keep the first non-None.
            value = next((v for _c, v in items if v is not None), None)
            if value is None:
                continue
            result.append((ec, byname, value))
    return result


def _cat_key_str(cat_key):
    """Render a category key (str or tuple) as a single Map index string."""
    if cat_key is None:
        return ""
    if isinstance(cat_key, tuple):
        return "__".join(str(x) for x in cat_key)
    return str(cat_key)


# ---------------------------------------------------------------------------
# Column iteration / entity byname construction
# ---------------------------------------------------------------------------

def _iter_columns(df, spec, s):
    """Yield ``(byname, category_key, series)`` for each data column.

    ``byname`` is the entity byname tuple (schema dim order, ``updown``
    normalized to ``upDown``).  ``category_key`` is the remaining column
    levels (or None).  ``series`` is the index->value Series for that column.
    """
    entity_dims = spec["entity_dims"]
    topology = spec.get("topology")
    reserve_split = spec.get("reserve_split", False)
    co2_type = spec.get("co2_type")
    entity_filter = spec.get("entity_filter")

    units = set(getattr(s, "process_unit", []))
    connections = set(getattr(s, "process_connection", []))

    # Fixed-entity (cost) frames: every column is a category.
    if entity_dims == 0:
        fixed = spec["fixed_entity"]
        for col in df.columns:
            yield fixed, col, df[col], None
        return

    triple_lookup = None
    if topology == "connection_triple":
        triple_lookup = _connection_triple_lookup(s)

    for col in df.columns:
        col_t = col if isinstance(col, tuple) else (col,)

        if co2_type is not None:
            # columns: (type, process, commodity, node)
            ctype, process, commodity, node = col_t
            if ctype != co2_type:
                continue
            yield (process,), (commodity, node), df[col], None
            continue

        if topology == "connection_triple":
            conn = col_t[0]
            triple = triple_lookup.get(conn)
            if triple is None:
                logger.debug("spinedb: no topology for connection '%s' — skipped",
                             conn)
                continue
            yield triple, None, df[col], None
            continue

        if reserve_split:
            # columns: (process, reserve, updown, node)
            process, reserve, updown, node = col_t
            byname = (process, reserve, _norm_updown(updown), node)
            if process in units:
                yield byname, None, df[col], "unit__reserve__upDown__node"
                continue
            if process in connections:
                yield byname, None, df[col], "connection__reserve__upDown__node"
                continue
            logger.debug("spinedb: reserve process '%s' neither unit nor "
                         "connection — skipped", process)
            continue

        if entity_filter == "unit" and col_t[0] not in units:
            continue
        if entity_filter == "connection" and col_t[0] not in connections:
            continue

        entity_part = col_t[:entity_dims]
        cat_part = col_t[entity_dims:]
        byname = tuple(_norm_updown(x) for x in entity_part)
        cat = cat_part[0] if len(cat_part) == 1 else (cat_part or None)
        if isinstance(cat, tuple) and not cat:
            cat = None
        yield byname, cat, df[col], None


def _norm_updown(value):
    """Normalize the ``updown`` label to schema's ``upDown`` spelling."""
    if value == "updown":
        return "upDown"
    if value == "up":
        return "up"
    if value == "down":
        return "down"
    return value


# ---------------------------------------------------------------------------
# Series -> Map
# ---------------------------------------------------------------------------

def _series_to_map(series, has_time, cat_key, *, collapse_solve, solve_name):
    """Convert a Series indexed by ``[solve,] period [, time]`` into a
    nested Map ``[solve ->] period [-> time] -> value``.

    ``collapse_solve`` aggregates the whole frame under a single
    ``solve_name`` key (cost_discounted_solve — the engine frame lacks a
    per-row solve axis).  Empty / all-NaN cells are dropped.
    """
    names = list(series.index.names)

    if collapse_solve:
        inner = _build_period_map(series, has_time, names)
        if inner is None:
            return None
        return Map([str(solve_name)], [inner])

    if "solve" in names:
        solve_keys: list[str] = []
        solve_vals: list = []
        for solve, sub in series.groupby(level="solve", sort=False):
            sub = sub.droplevel("solve")
            inner = _build_period_map(sub, has_time,
                                      [n for n in names if n != "solve"])
            if inner is None:
                continue
            solve_keys.append(str(solve))
            solve_vals.append(inner)
        if not solve_keys:
            return None
        return Map(solve_keys, solve_vals)

    # No solve level (could not be reconstructed) — wrap under solve_name.
    inner = _build_period_map(series, has_time, names)
    if inner is None:
        return None
    return Map([str(solve_name)], [inner])


def _build_period_map(series, has_time, names):
    """Build ``period -> [time ->] value`` from a Series whose index is
    ``period [, time]`` (no solve level)."""
    if has_time and "time" in names:
        period_keys: list[str] = []
        period_vals: list = []
        for period, sub in series.groupby(level="period", sort=False):
            sub = sub.droplevel("period") if sub.index.nlevels > 1 else sub
            t_keys: list[str] = []
            t_vals: list[float] = []
            for time_idx, val in sub.items():
                if _is_empty(val):
                    continue
                t_keys.append(str(time_idx))
                t_vals.append(float(val))
            if not t_keys:
                continue
            period_keys.append(str(period))
            period_vals.append(Map(t_keys, t_vals))
        if not period_keys:
            return None
        return Map(period_keys, period_vals)

    # period -> value
    p_keys: list[str] = []
    p_vals: list[float] = []
    if "period" in names and series.index.nlevels > 1:
        series = series.groupby(level="period", sort=False).first()
    for period_idx, val in series.items():
        if _is_empty(val):
            continue
        p_keys.append(str(period_idx))
        p_vals.append(float(val))
    if not p_keys:
        return None
    return Map(p_keys, p_vals)


def _is_empty(val) -> bool:
    """True for NaN / None values that must not be emitted."""
    if val is None:
        return True
    try:
        return bool(math.isnan(val))
    except (TypeError, ValueError):
        return False


# ---------------------------------------------------------------------------
# Solve reconstruction (mirrors the CSV branch, write_outputs.py:940-978)
# ---------------------------------------------------------------------------

def _ensure_solve_level(df, s, has_time):
    """Add a ``solve`` index level to ``df`` if it lacks one, mirroring the
    CSV branch logic.  Returns ``df`` unchanged when ``solve`` is already
    present or no ``period`` level exists."""
    names = list(df.index.names)
    if "solve" in names or "period" not in names:
        return df
    df = df.copy()
    if has_time and "time" in names:
        spt = s.solve_period_time
        if spt.droplevel("solve").duplicated().any():
            spt = spt[~spt.droplevel("solve").duplicated(keep="last")]
        pt_to_solve = dict(
            zip(spt.droplevel("solve"), spt.get_level_values("solve")))
        # Key off the named 'period'/'time' levels, not positional index
        # slots — entity-first indexes (e.g. (unit, period, time)) otherwise
        # read the wrong levels and miss every lookup.
        period_level = df.index.get_level_values("period")
        time_level = df.index.get_level_values("time")
        solve_vals = [pt_to_solve.get((p, t), "")
                      for p, t in zip(period_level, time_level)]
        df.index = pd.MultiIndex.from_arrays(
            [solve_vals] + [df.index.get_level_values(n) for n in df.index.names],
            names=["solve"] + list(df.index.names),
        )
    else:
        unique_sp = s.solve_period[
            ~s.solve_period.droplevel("solve").duplicated(keep="last")]
        period_to_solve = dict(zip(unique_sp.get_level_values("period"),
                                   unique_sp.get_level_values("solve")))
        if df.index.nlevels == 1:
            solve_vals = df.index.map(lambda p: period_to_solve.get(p, ""))
            df.index = pd.MultiIndex.from_arrays(
                [solve_vals, df.index], names=["solve", df.index.name])
        else:
            period_level = df.index.get_level_values("period")
            solve_vals = period_level.map(lambda p: period_to_solve.get(p, ""))
            df.index = pd.MultiIndex.from_arrays(
                [solve_vals] + [df.index.get_level_values(n)
                                for n in df.index.names],
                names=["solve"] + list(df.index.names),
            )
    return df


# ---------------------------------------------------------------------------
# Connection topology
# ---------------------------------------------------------------------------

def _connection_triple_lookup(s):
    """Build ``connection -> (connection, node, node)`` from
    ``s.process_source_sink`` filtered to connections.

    The frames keyed by a bare connection name (``connection_dt_eee`` etc.)
    need the (source, sink) nodes to form the ``connection__node__node``
    byname.  The first matching ``(process, source, sink)`` row wins."""
    lookup: dict[str, tuple] = {}
    pss = getattr(s, "process_source_sink", None)
    connections = set(getattr(s, "process_connection", []))
    if pss is None:
        return lookup
    for process, source, sink in pss:
        if process not in connections:
            continue
        if process not in lookup:
            lookup[process] = (process, source, sink)
    return lookup


